#include "../cub3D.h"

int	is_good_char(t_data *data, char c, int x, int y)
{
	if (c == 'N' || c == 'S' || c == 'W' || c == 'E')
	{
		
		data->player++;
		data->pos_x = x;
		data->pos_y = y;
	}
	if (c == ' ' || c == 'N' || c == 'S'
		|| c == 'W' || c == 'E' || c == '1'
		|| c == '0')
		return (0);
	return (1);	
}


